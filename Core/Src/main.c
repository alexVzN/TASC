/**
  * @brief  Function implementing the defaultTask thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_StartDefaultTask */
void StartDefaultTask(void *argument)
{
  /* USER CODE BEGIN 5 */
  /* Infinite loop */
	RTOSWD_add();
	IndicatorsDriver_setStatusForAll(kLoad);
	while (Outputs_get3WayValveOnResettingPosition())
	{
		osDelay(1000 / portTICK_PERIOD_MS);
		RTOSWD_update();
	}

	bool isMatching = false;
	for (;;)
	{
		if (StateInputs_isMatchMode())
		{
			if (isMatching) {
				ML_match();
			} else {
				isMatching = ML_startMatch();	
			}
		} else {
			if (isMatching) {
				isMatching = false;
				ML_stopMatch();
			} else {
				ML_controll();	
			}
		}

		if (StateInputs_resetDevice()) {
			MM_reset();
			NVIC_SystemReset();
		}

		osDelay(10 / portTICK_PERIOD_MS);
		RTOSWD_update();
	}

	RTOSWD_remove();
	osThreadTerminate(osThreadGetId());
  /* USER CODE END 5 */
}

